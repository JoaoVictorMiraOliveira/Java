package curso_udemy;

public class OperadoresAritimeticos {

	public static void main(String[] args) {

		int n1 = 3 + 4 * 2;
		int n2 = (3 + 4) * 2;
		int n3 = 17 % 3;	
		double n4 = 10.0 / 8;
		double n5 = (double) 10 / 8;
		
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		System.out.println(n4);
		System.out.println(n5);
		
	}

}
