package curso_udemy;

public class OperadoresLogicos {

	public static void main(String[] args) {

		/*
		 * Operadores Logicos : && (E) ; || (OU) ; ! (NÃO) ;
		 */

		boolean c1 = 2 > 3 || 4 != 5;
		boolean c2 = !(2 > 3) && 4 != 5;
		
		System.out.println(c1);
		System.out.println(c2);
		
	}

}
